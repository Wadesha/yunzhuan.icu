def generate_steps(course_key, course_name):
    """根据课程名称生成专业的5步实施步骤"""
    
    # 解析课程名和项目名
    parts = course_name.split('-')
    course_cn = parts[0]
    project_cn = parts[1] if len(parts) > 1 else course_cn
    
    # 确定大类
    group = course_key.split('-')[0]
    
    # 根据大类生成不同的步骤
    steps = get_group_steps(group, course_key, course_cn, project_cn)
    return steps


