def process(fp, key):
    with open(fp, 'r', encoding='utf-8') as f:
        c = f.read()
    n = build_html(key)
    if not n:
        return False
    p = r'(<h2>实施步骤 <span class="en">Implementation Steps</span></h2>\s*<div>\s*).*?(?=\s*</div>\s*</div>\s*<div class="course-nav)'
    nc = re.sub(p, r'\1' + n, c, flags=re.DOTALL)
    with open(fp, 'w', encoding='utf-8') as f:
        f.write(nc)
    return True


